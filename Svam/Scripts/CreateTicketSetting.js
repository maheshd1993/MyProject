
/////////////subject click function///////////////////////
$("#Issubject").click(function () {
    if ($(this).is(":checked")) {
        $("#divSubject").show();
        //console.log("Checkbox is checked.");
    }
    else if ($(this).is(":not(:checked)")) {
        $("#divSubject").hide();
        $("#SubNo").prop("checked", true);
        //console.log("Checkbox is unchecked.");
    }
});


